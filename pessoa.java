public class pessoa {
    
    private int coordX, coordY, largura, altura;
    private int head, body, armleft, armright, legLeft, legRight;
    
    

    public int getCoordX() {
        return coordX;
    }

    public void setCoordX(int coordX) {
        this.coordX = coordX;
    }

    public int getCoordY() {
        return coordY;
    }

    public void setCoordY(int coordY) {
        this.coordY = coordY;
    }

    public int getLargura() {
        return largura;
    }

    public void setLargura(int largura) {
        this.largura = largura;
    }

    public int getAltura() {
        return altura;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }

    public int getHead() {
        return head;
    }

    public void setHead(int head) {
        this.head = head;
    }

    public int getBody() {
        return body;
    }

    public void setBody(int body) {
        this.body = body;
    }

    public int getArmleft() {
        return armleft;
    }

    public void setArmleft(int armleft) {
        this.armleft = armleft;
    }

    public int getArmright() {
        return armright;
    }

    public void setArmright(int armright) {
        this.armright = armright;
    }

    public int getLegLeft() {
        return legLeft;
    }

    public void setLegLeft(int legLeft) {
        this.legLeft = legLeft;
    }

    public int getLegRight() {
        return legRight;
    }

    public void setLegRight(int legRight) {
        this.legRight = legRight;
    }
    
    
    
}
